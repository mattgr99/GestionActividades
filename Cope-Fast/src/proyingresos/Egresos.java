    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        PreparedStatement ps = null;
        try {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();
            ps = conn.prepareStatement("INSERT INTO egresos (cedula, nombre, motivo_gasto, valor_gasto) VALUES (?,?,?,?)");
            ps.setString(1, txtcedula1.getText());
            ps.setString(2, txtnombre.getText());
            ps.setString(3, comunes.getSelectedItem().toString());
            ps.setString(4, txtvalor.getText());
            ps.execute();

            JOptionPane.showMessageDialog(null, "Gasto registrada");
            Object[] fila = new Object[4];
            fila[0] = txtcedula1.getText();
            fila[1] = txtnombre.getText();
            fila[2] = comunes.getSelectedItem().toString();
            fila[3] = txtvalor.getText();
           
            modelo.addRow(fila);
            

            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Registrar gasto");
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


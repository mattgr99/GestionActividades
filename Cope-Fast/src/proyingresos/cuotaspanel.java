public void tabla(){
    DefaultTableModel modelo = new DefaultTableModel();
    try {
            jtcuotas.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            java.sql.Connection con = conn.getConexion();

            String sql = "SELECT cedula, nombre, valor_cuota, motivo_cuota, fecha_pago FROM cuotas";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Cedula");
            modelo.addColumn("Nombre");
            modelo.addColumn("Valor cuota");
            modelo.addColumn("Motivo cuota");
            modelo.addColumn("Fecha pago");

            int[] anchos = {50, 40, 50, 140,60};
            for (int i = 0; i < jtcuotas.getColumnCount(); i++) {
                jtcuotas.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
}
